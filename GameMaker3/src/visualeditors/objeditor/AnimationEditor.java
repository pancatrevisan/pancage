    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        // TODO add your handling code here:
        //load anim.
        JFileChooser jfc = new JFileChooser(Config.ANIM_PATH);
        int op = jfc.showSaveDialog(this);
        if (op != JFileChooser.CANCEL_OPTION)
        {
            String f = jfc.getSelectedFile().getAbsolutePath();
            animation = Animation.load(f);
            for(Sprite s:animation.getSprites())
                spriteList.addElement(s);
            for(int i:animation.getSpriteOrder())
                orderAnim.addElement(i);
            spritesAligned = true;
            sfx = animation.getSFX().getName();
            jCheckBox1.setSelected(animation.isLoop());
            jCheckBox2.setSelected(animation.isLoopSFX());
        }
        
    }//GEN-LAST:event_jMenuItem1ActionPerformed

