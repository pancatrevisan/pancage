    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(jList2.getSelectedIndex()<jList2.getModel().getSize()-1)
        {
            Integer i = (Integer) jList2.getSelectedValue();
            int idx = jList2.getSelectedIndex();
            int newI = i.intValue();
            
            int next = idx+1;
            Integer nextE = (Integer) jList2.getModel().getElementAt(next);
            int nextV = nextE.intValue();
            
            //change newI and nextV
            orderAnim.remove(idx);//remove elem.
            orderAnim.remove(idx);//the next becomes the new,
            
            orderAnim.add(idx, nextV);
            orderAnim.add(idx+1, newI);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

