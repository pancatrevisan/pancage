    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(!spritesAligned)
        {
            JOptionPane.showMessageDialog(this, "Align sprites first!");
            return;
        }
        Sprite[] s = new Sprite[spriteList.size()];
        for(int i = 0; i < spriteList.size(); i++)
            s[i] = (Sprite) spriteList.elementAt(i);
        int[] order = new int[orderAnim.size()];
        for(int i = 0; i < orderAnim.size(); i++)
            order[i] = (int) orderAnim.elementAt(i);
        SFX sfx0 = new SFX(sfx);
        Animation an = new Animation(s,order,true,sfx0);
        an.setLoopSFX(jCheckBox2.isSelected());
        an.setLoop(jCheckBox1.isSelected());
        final TestAnimationGame tbg = new TestAnimationGame(640, 480,640,480,60, an,200,200);
        TestFrame t = new TestFrame(tbg);
        
        t.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        
        t.setSize(640, 480);
        t.setVisible(true);
        
        new Thread(t).start();
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
        // TODO add your handling code here:
        //save
        JFileChooser jfc = new JFileChooser(Config.ANIM_PATH);
        int op = jfc.showSaveDialog(this);
        if(!spritesAligned)
        {
            JOptionPane.showMessageDialog(this, "Align sprites first!");
            return;
        }
        Sprite[] s = new Sprite[spriteList.size()];
        for(int i = 0; i < spriteList.size(); i++)
            s[i] = (Sprite) spriteList.elementAt(i);
        int[] order = new int[orderAnim.size()];
        for(int i = 0; i < orderAnim.size(); i++)
            order[i] = (int) orderAnim.elementAt(i);
        animation.setSprites(s);
        animation.setSpriteOrder(order);
        animation.setLoop(jCheckBox1.isSelected());
        SFX sfx0 = new SFX(sfx);
        animation.setSFX(sfx0);
        if (op != JFileChooser.CANCEL_OPTION)
        {
            
            String f = jfc.getSelectedFile().getAbsolutePath();
            
            for(int i = 0; i < spriteList.size(); i++)
                
            this.animation.save(animation, f);
        }
    }//GEN-LAST:event_jMenuItem2ActionPerformed

